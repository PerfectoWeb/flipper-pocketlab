const char* pocketlab_tr(const char* en) {
    if(!en) return "";
    if(pocketlab_i18n_get_lang() != PocketLabLangRu) return en;
    for(size_t i = 0; i < COUNT_OF(translations); i++) {
        if(strcmp(translations[i].en, en) == 0) {
            return translations[i].ru[0] ? translations[i].ru : en;
        }
    }
    return en;
}
