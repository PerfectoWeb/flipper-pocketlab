const uint8_t* pocketlab_font(bool bold) {
    // Only the bold face is used; the compact 6x12 stands in for "regular".
    return bold ? pocketlab_font_6x13B : pocketlab_font_6x12;
}

static bool s_universal = false;

void pocketlab_font_set_universal(bool universal) {
    s_universal = universal;
}

bool pocketlab_font_is_universal(void) {
    return s_universal;
}

void pocketlab_font_apply(Canvas* canvas, bool bold) {
    if(s_universal) {
        canvas_set_custom_u8g2_font(canvas, pocketlab_font(bold));
    } else {
        canvas_set_font(canvas, bold ? FontPrimary : FontSecondary);
    }
}

void pocketlab_font_apply_small(Canvas* canvas) {
    if(s_universal) {
        canvas_set_custom_u8g2_font(canvas, pocketlab_font_6x12);
    } else {
        canvas_set_font(canvas, FontSecondary);
    }
}
